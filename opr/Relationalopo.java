package opr;

public class Relationalopo {
    public static void main(String[] args) {
        int A = 20;
        int B = 20;
        System.out.println(A==B);// True
        System.out.println(A!=B);// false
        System.out.println(A>B);//false
        System.out.println(A<B);//false
        System.out.println(A>=B);//True
        System.out.println(A<=B);//True



    }
}
