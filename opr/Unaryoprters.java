package opr;

public class Unaryoprters {
    public static void main(String [] args){
        int A = 25;
        boolean B = true;
        System.out.println(A++);// A = 25 +1 = 26
        System.out.println(++A);// 26+1 A = 27
        System.out.println(A--);// A =26
        System.out.println(--A); // A = 25
        System.out.println(!B); // true


    }
}
