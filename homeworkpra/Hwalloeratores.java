package homeworkpra;

public class Hwalloeratores {
    public static void main(String[] args){
        int H = 24;
        int N = 27;
        System.out.println(H+N); // 51
        System.out.println(H-N); // -3
        System.out.println(H*N); // 648
        System.out.println(H/N); //  0
        System.out.println(H%N); // 0
        System.out.println(H++); // 24
        System.out.println(++H); // 26
        System.out.println(N--); //27
        System.out.println(--N);//25


    }
}
